const LocaleContext = createContext<LocaleContextValue | null>(null);

export type { Locale };

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>(() => {
    const stored = typeof window !== "undefined" ? window.localStorage.getItem("portfy-locale") : null;
    if (stored === "en" || stored === "es" || stored === "pt") {
      return stored;
    }
    return "pt";
  });

  const value = useMemo<LocaleContextValue>(() => ({
    locale,
    setLocale: (next) => {
      setLocale(next);
      if (typeof window !== "undefined") {
        window.localStorage.setItem("portfy-locale", next);
      }
    },
    t: (key) => translations[locale][key] ?? translations.pt[key] ?? key,
  }), [locale]);

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export function useLocale() {
  const ctx = useContext(LocaleContext);
  if (!ctx) {
    throw new Error("useLocale must be used within LocaleProvider");
  }
  return ctx;
}
